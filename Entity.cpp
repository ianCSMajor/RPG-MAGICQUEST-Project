void Entity::attack(Entity* _target){
  this->_atkType->doAttack(this, _target);
}

void Entity::setAttack(Attack* atk){ 
  if (_atkType != nullptr) {
    delete _atkType;
  }
  this->_atkType= atk; 
  // this->weapon= atk->atkName;
}

Attack* Entity::getAttack() {
  return _atkType;
}


